					identificado como Torre Marfil Unidad Funcional No. B-1-2, 
					matrícula No.0200142138, ubicado en el sector del Embrujo I de 
					esta de Santiago de los Caballeros, el cual a sido visto y 
					encontrado a entera satisfacción por la INQUILINA, quien lo 
					usara para fines exclusivamente como vivienda, no pudiendo 
					dedicarlo a otros fines, ni cederlos sin el consentimiento escrito 
					de EL PROPIETARIO”</p>
				<p align="justify"><b>SEGUNDO: Precio del alquiler.</b> La cantidad convenida como 
					precio del presente inquilinato es la suma de <b>RDS26,000.00
						(VEINTISEIS MIL PESOS CON 00/100)</b> durante los primeros seis meses y la suma de <b>RDS28,000.00 (VEINTIOCHO MIL PESOS 
					CON 00/100)</b> para los próximos seis meses del presente acto, 
					pagaderos mensuales a más tardar los días 15 de cada mes; 
					Esta suma incluye el pago de la cuota mensual del 
					mantenimiento de las áreas comunes.</p>
				<p align="justify">2.1. <b>Depósito. LA INQUILINA mantiene en calidad de depósito,</b> la
					suma de <b>RD$44,000.00 (cuarenta y cuatro mil pesos con 00/100)</b>
					equivalente a una proporción de la renta anual como garantía de
					cumplimiento de sus obligaciones asumidas en este acuerdo.
					Por lo tanto, no podrá pretender en ningún caso mientras ocupe
					el apartamento aplicar el avance a renta al pago de alquiler del
					mes vencido. Esta inversión solo podrá hacerla EL
					PROPIETARIO después de haber sido desocupado el inmueble
					con la finalidad de cubrir los gastos por daños que este hubiese
					ocasionado a dicho inmueble. Si todo está en perfectas
					condiciones su depósito se devolverá integro posterior a la
					comprobación.</p>
				<p align="justify">2.2. Queda debidamente convenido que al momento mismo de
					concluir este contrato y, al efectuarse la devolución de las llaves
					del inmueble objeto del alquiler y la entrega del mismo, deberá
					realizarse la revisión por parte de EL PROPIETARIO; el depósito
					quedará retenido hasta tanto no se haya comprobado que <b>LA
						INQUILINA</b> ha cumplido con todas las cláusulas del presente
					contrato. Se establece, además, que el inmueble objeto del
					presente inquilinato, será recibido por EL PROPIETARIO en las
					mismas condiciones en que le fue entregado A <b>LA INQUILINA.</b></p>
				<p align="justify"><b>TERCERO:</b> Duración del contrato. Este contrato se conviene por
					el término un (1) año, a partir de la firma de este acuerdo, sin
					embargo, las obligaciones de LA INQUILINA persistirán hasta el
					momento mismo en que real y efectivamente entregue A EL
					PROPIETARIO las llaves del inmueble alquilado. </p>
				<p align="justify">3.1. Terminación anticipada por el propietario e inquilina. El
					propietario e inquilina, tendrán la facultad de poner termino
					anticipado al contrato mediante notificación hecha por el escrito
					en el domicilio de elección de los mismos, sea por carta
					certificadas o por acto de alguacil.</p>
				<p align="justify">3.2 Tacita Reconducción. Queda expresamente entendido y
					acordado entre las partes, que este contrato no operara la
					Tacita Reconducción, por lo que, al llegar a término, el mismo
					quedara rescindido de pleno derecho, debiendo LA INQUILINA,
					desocupar el inmueble alquilado, inmediatamente y sin
					necesidad de requerimiento previo de carácter judicial o
					extrajudicial de parte de EL PROPIETARIO, a no ser por voluntad
					de las partes opere la renovación del mismo</p>
				<p align="justify">3.3. En caso de que <b>LA INQUILINA</b> abandone el inmueble
					alquilado, sin entregar las llaves a EL PROPIETARIO, ésta tendrá
					plenos derechos a considerar concluido el contrato. Se
					considera que hubo abandono, después de transcurrido cinco
					(5) días a partir de la notificación que hiciese EL PROPIETARIO, haciéndole el requerimiento a <b></b>LA INQUILINA en su domicilio de
					elección</p>
				<p align="justify"><b>CUARTO: LA INQUILINA</b> , por medio del presente contrato queda
					obligada a mantener el inmueble en buen estado y todos los
					desperfectos en paredes, pisos, cisterna, puertas, ventanas,
					cristales, pestillos, cerraduras, instalación eléctrica y sanitaria
					(obstrucción de inodoros, lavamanos, fregadero y cualquier otro
					desagüe, cambio de zapatillas, roturas de llaves), y que se
					deban al uso ordinario del inmueble, van por cuenta de LA
					INQUILINA, salvo la reparación de aquellos desperfectos
					ocasionados en edificaciones estructurales. También queda a
					cargo de LA INQUILINA la pintura interior de la propiedad.
					Además, LA INQUILINA tendrá el inmueble libre de animales
					dañinos. 
					</p>
				<p align="justify">4.1. Queda convenido entre las partes, que EL PROPIETARIO
					tendrán la facultad de supervisar el inmueble alquilado por lo
					menos mensualmente; que <b>LA INQUILINA</b> acepta y presta su
					consentimiento y se someterá a las reglas del condominio. 
					</p>
				<p align="justify">4.2. Los defectos pertenecientes a la construcción del inmueble
